const LANG_KEY = 'lang';
const DEFAULT_LANG = 'ar';

export function getLang() {
  const stored = getRaw(LANG_KEY, '');
  return translations[stored] ? stored : DEFAULT_LANG;
}

export function setLang(lang) {
  if (!translations[lang]) return;
  setRaw(LANG_KEY, lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}

/** ترجمة مفتاح؛ يعيد المفتاح نفسه إن لم توجد ترجمة (لتسهيل اكتشاف النواقص) */
export function t(key) {
  const lang = getLang();
  return translations[lang]?.[key] ?? translations[DEFAULT_LANG]?.[key] ?? key;
}

/** يطبّق lang/dir على عنصر <html> عند بدء التشغيل */
export function applyDocumentDirection() {
  const lang = getLang();
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
}
